// Therefore, the ranges of the digits are:
//      d01: 1 - 6
//      d02: 4 - 9
//      d03: 9    
//      d04: 1    
//      d05: 1 - 4
//      d06: 6 - 9
//      d07: 7 - 9
//      d08: 5 - 9
//      d09: 3 - 9
//      d10: 1 - 7
//      d11: 1 - 5
//      d12: 1 - 3
