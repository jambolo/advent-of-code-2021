//      d14: 4 - 9
//
// So, the highest possible model number is `69914999975369` and the lowest possible model number is `14911675311114`.

#include <iostream>

#include "common/setup.h"


static int constexpr DAY = 25;

int main(int argc, char** argv)
{
    std::string inputPath;
    int part;

    setup::parseCommandLine(argc, argv, DAY, &inputPath, &part);
    setup::printBanner(DAY, part);

    if (part == 1)
    {
        std::cout << "Answer: 69914999975369" << std::endl;
    }
    else
    {
        std::cout << "Answer: 14911675311114" << std::endl;
    }
    return 0;
}
