// In order for z14 == 0, the following must be true:
//      d04 == d03 - 8      
//      d06 == d05 + 5
//      d10 == d09 - 2
//      d11 == d08 - 4
//      d12 == d07 - 6
//      d13 == d02 - 3
//      d14 == d01 + 3
